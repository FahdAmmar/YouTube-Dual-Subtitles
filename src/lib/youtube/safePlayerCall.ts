/**
 * غلاف دفاعي موحّد لاستدعاءات واجهة مشغّل يوتيوب (YT.Player)
 *
 * لماذا هذا ضروري: كل دالة في YT.Player (getCurrentTime, getDuration,
 * playVideo...) ليست استدعاءً محلياً بسيطاً — إنها جسر postMessage بين
 * صفحتنا وإطار iframe خارجي مستضاف على domain مختلف بالكامل (يوتيوب).
 * هذا الجسر عرضة لانقطاعات حقيقية لا علاقة لها بكودنا: تقييد المتصفح
 * لعلامات التبويب غير النشطة (tab throttling)، انقطاع شبكة مؤقت، أو حتى
 * إعادة بناء يوتيوب الداخلية لإطار iframe لسبب غير متعلق بنا إطلاقاً —
 * وكل هذه الحالات تصبح أكثر احتمالاً كلما طالت مدة التشغيل، ما يفسّر
 * ظهور المشكلة تحديداً "بعد فترة من التشغيل" وليس فوراً عند البداية.
 *
 * الأخطر: بعض هذه الاستدعاءات (تحديداً getCurrentTime عبر usePlayerTime)
 * تقع داخل getSnapshot الخاصة بـ useSyncExternalStore، والتي يستدعيها
 * React أثناء طور الرسم (Render) مباشرة — وأي استثناء غير مُمسوك هناك
 * يصل فوراً إلى ErrorBoundary الجذري ويستبدل الصفحة بأكملها. التغليف هنا
 * يضمن أن فشل استدعاء واحد مؤقت لا يُسقط التطبيق كله أبداً، بل يُرجع
 * قيمة احتياطية آمنة ويستمر التشغيل بشكل طبيعي في المرة التالية
 */
export function safePlayerCall<T>(fn: () => T, fallback: T): T {
  try {
    return fn()
  } catch {
    return fallback
  }
}
