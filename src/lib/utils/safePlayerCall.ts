/**
 * غلاف دفاعي موحّد لأي استدعاء تحكّم بمشغّل فيديو — سواء كان مشغّل يوتيوب
 * (YT.Player) أو عنصر <video> محلي — يُرجع قيمة احتياطية آمنة بدل السماح
 * لاستثناء غير متوقَّع بإسقاط التطبيق بأكمله
 *
 * لماذا هذا ضروري لمشغّل يوتيوب تحديداً: كل دالة في YT.Player
 * (getCurrentTime, getDuration, playVideo...) ليست استدعاءً محلياً بسيطاً
 * — إنها جسر postMessage بين صفحتنا وإطار iframe خارجي مستضاف على domain
 * مختلف بالكامل (يوتيوب). هذا الجسر عرضة لانقطاعات حقيقية لا علاقة لها
 * بكودنا: تقييد المتصفح لعلامات التبويب غير النشطة (tab throttling)،
 * انقطاع شبكة مؤقت، أو حتى إعادة بناء يوتيوب الداخلية لإطار iframe لسبب
 * غير متعلق بنا إطلاقاً — وكل هذه الحالات تصبح أكثر احتمالاً كلما طالت
 * مدة التشغيل، ما يفسّر ظهور بعض المشكلات تحديداً "بعد فترة من التشغيل"
 * وليس فوراً عند البداية.
 *
 * ولمشغّل الفيديو المحلي أيضاً: رغم أن استدعاءات عنصر <video> محلية
 * (بلا postMessage)، فبعضها لا يزال عرضة للفشل بشكل مشروع — أبرزها
 * video.play() التي تُعيد Promise قد يُرفض (NotAllowedError بسبب سياسات
 * التشغيل التلقائي، أو AbortError عند استدعاء pause() بسرعة بعدها).
 *
 * الأخطر: بعض هذه الاستدعاءات (تحديداً getCurrentTime عبر usePlayerTime)
 * تقع داخل getSnapshot الخاصة بـ useSyncExternalStore، والتي يستدعيها
 * React أثناء طور الرسم (Render) مباشرة — وأي استثناء غير مُمسوك هناك
 * يصل فوراً إلى ErrorBoundary الجذري ويستبدل الصفحة بأكملها. التغليف هنا
 * يضمن أن فشل استدعاء واحد مؤقت لا يُسقط التطبيق كله أبداً، بل يُرجع
 * قيمة احتياطية آمنة ويستمر التشغيل بشكل طبيعي في المرة التالية
 */
export function safePlayerCall<T>(fn: () => T, fallback: T): T {
  try {
    return fn()
  } catch {
    return fallback
  }
}
